                    Each night before drifting off to sleep repeat the phrase 'I will remember my dreams as soon as I wake up'. Say this phrase over and over until you fall asleep, after a few days you will start to remember your dreams much more vividly.
                    As well as repeating affirmations, such as 'I will lucid dream' throughout your day. This will help boost progress towards lucid dreaming.
